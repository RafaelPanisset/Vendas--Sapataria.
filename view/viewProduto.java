    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        modelProduto = new ModelProduto();
        int linha = produto_tabela.getSelectedRow();

        if (linha < 0) {
            JOptionPane.showMessageDialog(this, "Selecione algum nome na tabela", "Aviso", JOptionPane.INFORMATION_MESSAGE);
        } else {

            int certeza = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir o produto?");
            if (certeza == JOptionPane.OK_OPTION) {

                int codigo = (int) produto_tabela.getValueAt(linha, 0);
                if (controllerModelProdutos.excluirProdutoController(codigo)) {
                    JOptionPane.showMessageDialog(this, "Excluido com sucesso!", "Aviso", JOptionPane.INFORMATION_MESSAGE);

                } else {
                    JOptionPane.showMessageDialog(this, "O produto não pode ser excluido!", "Aviso", JOptionPane.INFORMATION_MESSAGE);

                }

            }
            this.carregarProdutos();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

