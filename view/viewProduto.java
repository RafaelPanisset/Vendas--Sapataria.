    private void carregarProdutos() {

        ProdutosDetalhes produtosDetalhes = new ProdutosDetalhes();
        listaModelProduto = new ArrayList<>();
        listaModelProduto = controllerModelProdutos.getListaProdutoController();

        DefaultTableModel modelo = (DefaultTableModel) produto_tabela.getModel();
        modelo.setRowCount(0);
        // inserir prodtus na tabela
        for (int i = 0; i < listaModelProduto.size(); i++) {
            modelo.addRow(new Object[]{
                listaModelProduto.get(i).getIdProduto(),
                listaModelProduto.get(i).getProNome(),
                listaModelProduto.get(i).getProEstoque(),
                listaModelProduto.get(i).getProValor()

            });
        }

    }

