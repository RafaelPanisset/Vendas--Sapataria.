    private void del_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_del_salvarActionPerformed
        // TODO add your handling code here:

        if (SalvarAlterar.equals("salvar")) {

            modelDetalhes = new ModelDetalhes();
            modelDetalhes.setPkIdDetalhe(8);
            modelDetalhes.setPkIdProdutos(5);
            modelDetalhes.setpNome("ff");
            modelDetalhes.setTblCor(del_cor.getText());
            modelDetalhes.setTblTamanho(Integer.valueOf(del_tamanho.getText()));
            modelDetalhes.setTblImagem(caminho);
            modelDetalhes.setTblMarca("gggg");
            System.out.println(modelDetalhes);

            if (controllerDetalhes.salvarDetalhesController(modelDetalhes) > 0) {
                JOptionPane.showMessageDialog(this, "Salvo com sucesso", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                limpar();
                SalvarAlterar = "salvar";
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao salvar", "Aviso", JOptionPane.ERROR_MESSAGE);
                SalvarAlterar = "salvar";

            }
        } else if (SalvarAlterar.equals("alterar")) {
            modelDetalhes.setpNome("ff");
            modelDetalhes.setTblCor(del_cor.getText());
            modelDetalhes.setTblTamanho(Integer.valueOf(del_tamanho.getText()));
            modelDetalhes.setTblImagem(caminho);
            modelDetalhes.setTblMarca(del_marca.getText());

            if (controllerDetalhes.atualizarDetalhesController(modelDetalhes)) {
                JOptionPane.showMessageDialog(this, "Alterado com sucesso", "Aviso", JOptionPane.INFORMATION_MESSAGE);
                limpar();
                SalvarAlterar = "salvar";

            } else {
                JOptionPane.showMessageDialog(this, "Erro ao Alterar", "Aviso", JOptionPane.ERROR_MESSAGE);
                SalvarAlterar = "salvar";

            }

        }

    }//GEN-LAST:event_del_salvarActionPerformed

