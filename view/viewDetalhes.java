    private void del_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_del_alterarActionPerformed
        // TODO add your handling code here:

        SalvarAlterar = "alterar";
        modelDetalhes = new ModelDetalhes();
        String caminhoImagem;

        // código de produto
        int codigo = 8;

        modelDetalhes = controllerDetalhes.getDetalhesController(codigo);
        del_codigo.setText(String.valueOf(modelDetalhes.getPkIdProdutos()));

        del_tamanho.setText(String.valueOf(modelDetalhes.getTblTamanho()));
        del_cor.setText(modelDetalhes.getTblCor());
        del_marca.setText(modelDetalhes.getTblMarca());
        de_idde.setText(String.valueOf(modelDetalhes.getPkIdDetalhe()));
        del_nome.setText(modelDetalhes.getpNome());

        caminhoImagem = modelDetalhes.getTblImagem();
        int tamanhoDaString = caminhoImagem.length() + 2;
        char[] armazernarFoto = new char[tamanhoDaString];

        for (int i = 0, j = 0; j < caminhoImagem.length(); i++, j++) {

            // acrescentar "\" na array 1
            if (i == 2) {
                armazernarFoto[i] = 92;
                i = i + 1;

            }

            // acrescentar "\" na array 5
            if (i == 8) {
                armazernarFoto[i] = 92;
                i = i + 1;
            }

            armazernarFoto[i] = caminhoImagem.charAt(j);

        }

        // Converter a array em uma String
        String caminhoDaImagem = String.valueOf(armazernarFoto);
        System.out.println(caminhoDaImagem);
        //Retornando a foto 
        ImageIcon minhaImagem = new ImageIcon(caminhoDaImagem);

        Image img = minhaImagem.getImage();

        Image novaImage = img.getScaledInstance(cad_InserirFoto.getWidth(), cad_InserirFoto.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(novaImage);
        cad_InserirFoto.setIcon(image);
    }//GEN-LAST:event_del_alterarActionPerformed

