    public String receberImagem() throws IOException {

        caminho = new String();

        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("Imagens em JPEG E PNG", "jpg", "png");
        fileChooser.addChoosableFileFilter(filtro);

        fileChooser.setAcceptAllFileFilterUsed(false);

        fileChooser.setDialogType(JFileChooser.OPEN_DIALOG);
        fileChooser.setCurrentDirectory(new File("C:/Fotos"));
        fileChooser.showOpenDialog(this);

        caminho = fileChooser.getSelectedFile().getAbsolutePath();
//
//      imagemBuffer = ImageIO.read(new File(caminho));
//     Image diminuirImagem = imagemBuffer.getScaledInstance(290, 100, 0);
//       imagemLabel.setIcon(new ImageIcon(diminuirImagem));
        ImageIcon minhaImagem = new ImageIcon(caminho);

        Image img = minhaImagem.getImage();

        Image novaImage = img.getScaledInstance(cad_InserirFoto.getWidth(), cad_InserirFoto.getHeight(), Image.SCALE_SMOOTH);
        ImageIcon image = new ImageIcon(novaImage);
        cad_InserirFoto.setIcon(image);

        return caminho;

    }

