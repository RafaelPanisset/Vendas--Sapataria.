    private void entrar() {
        contaUsuarioMD = new ModelUsuario();
        ModelSessaoUsuario = new modelSessaoUsuario();

        contaUsuarioMD.setUsoLogin(login_nome.getText());
        contaUsuarioMD.setUsoSenha(new String(login_senha.getPassword()));

        if (!contaUsuarioCT.verificarSenha(contaUsuarioMD)) {
            JOptionPane.showMessageDialog(this, "Login ou senha inv√°lidos", "Aviso", JOptionPane.ERROR_MESSAGE);
        } else {
            contaUsuarioMD = contaUsuarioCT.getUsuarioLoginController(login_nome.getText());
            ModelSessaoUsuario.codigo = contaUsuarioMD.getIdUsuario();
            ModelSessaoUsuario.pLogin = contaUsuarioMD.getUsoLogin();
            ModelSessaoUsuario.pNome = contaUsuarioMD.getUsoSenha();
            new viewPrincipal().setVisible(true);

        }

    }

