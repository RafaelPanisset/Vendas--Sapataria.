    private void salvarFornecedor() throws Exception {

        modelFornecedor.setNomeFornecedor(this.for_nome.getText().toUpperCase());
        modelFornecedor.setEnderecoFornecedor(this.for_endereco.getText().toUpperCase());
        modelFornecedor.setBairroFornecedor(this.for_bairro.getText().toUpperCase());
        modelFornecedor.setUfFornecedor(this.for_uf.getSelectedItem().toString());
        modelFornecedor.setCepFornecedor(this.for_cep.getText());
        modelFornecedor.setTelefoneFornecedor(this.for_telefone.getText());
        modelFornecedor.setData(bLDatas.converterDataParaDateUS(new java.util.Date(System.currentTimeMillis())));

        if (pessoa1.isSelected()) {
            modelFornecedor.setPessoaFornecedor(0);
        }

        if (pessoa2.isSelected()) {
            modelFornecedor.setPessoaFornecedor(1);
        }

        if (controlerFornecedor.salvarFornecedorController(modelFornecedor) > 0) {
            JOptionPane.showMessageDialog(this, "Fornecedor salvo com sucesso!", "Aviso", JOptionPane.INFORMATION_MESSAGE);

            this.carregarFornecedor();
            this.limparTela();
            this.habilitarDesabilitar(false);

        } else {
            JOptionPane.showMessageDialog(this, "Erro ao cadastrar o fornecedor!", "Erro", JOptionPane.ERROR_MESSAGE);

        }

    }

