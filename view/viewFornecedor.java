    private void for_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_for_salvarActionPerformed
        // TODO add your handling code here:
        if (salvarAlterar.equals("salvar")) {
            try {
                salvarFornecedor();
            } catch (Exception ex) {
                Logger.getLogger(viewFornecedor.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else if (salvarAlterar.equals("alterar")) {
            alterarForncedor();
        }
    }//GEN-LAST:event_for_salvarActionPerformed

    private void for_alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_for_alterarActionPerformed
        // TODO add your handling code here:
        salvarAlterar = "alterar";
        habilitarDesabilitar(true);
        // TODO add your handling code here:
        int linha = this.for_tabela.getSelectedRow();

        try {
            int codigo = (int) for_tabela.getValueAt(linha, 0);
            modelFornecedor = controlerFornecedor.getFornecedorController(codigo);

            for_codigo.setText(String.valueOf(modelFornecedor.getIdForncedor()));
            for_nome.setText(String.valueOf(modelFornecedor.getNomeFornecedor()));
            for_endereco.setText(modelFornecedor.getEnderecoFornecedor());
            for_bairro.setText(String.valueOf(modelFornecedor.getBairroFornecedor()));
            for_uf.setSelectedItem(String.valueOf(modelFornecedor.getUfFornecedor()));
            for_cep.setText(modelFornecedor.getCepFornecedor());
            for_telefone.setText(String.valueOf(modelFornecedor.getTelefoneFornecedor()));

            if (modelFornecedor.getPessoaFornecedor() == 1) {
                pessoa2.setSelected(true);
            }
            if (modelFornecedor.getPessoaFornecedor() == 0) {
                pessoa1.setSelected(true);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Código inválido ou nem um registro selecionado!", "Aviso", JOptionPane.ERROR_MESSAGE);

        }
    }//GEN-LAST:event_for_alterarActionPerformed

